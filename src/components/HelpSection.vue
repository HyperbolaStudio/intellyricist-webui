    </NP>
    <NH2>限定关键词</NH2>
    <NP>
        如果选中，那么程序将不会向生成的歌词中扩充除「关键词列表」中给出的关键词以外的其他关键词；否则程序会向生成的歌词中扩充其他的与所给关键词相关的意象。
    </NP>
    <NP>
        仅当「启用关键词」选项被选中时可用。当「将选中行作为 Prompt」选项被选中，「前缀空行」不为 0 ，或者「行 Prompt」不为空时，「限定关键词」必须选中。
    </NP>
    <NH2>将选中行作为 Prompt</NH2>
    <NP>
        如果选中，程序将会以「歌词编辑器」面板中选中的歌词为基础进行续写。
    </NP>
