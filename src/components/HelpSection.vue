    </NP>
    <NP>
        程序所生成的歌词会呈现在歌词候选区内。呈现在歌词候选区内的歌词无法编辑，要想手动修改歌词，请按下「添加」按钮，即可将歌词从歌词候选区移动到歌词编辑器当中。
    </NP>
    <NP>
        请注意，如果按下「生成」按钮，歌词候选区内的歌词会自动清空，请确保在此之前将所需歌词移动到歌词编辑器当中。
    </NP>
    <NH1>歌词编辑器</NH1>
    <NP>
        歌词编辑器是用于存储和编辑生成的歌词的模块。歌词编辑器中的歌词可以来自歌词生成模块的歌词候选区，也可以手动创建和编辑。
    </NP>
    <NP>
        按下「生成参数」区域上方的「编辑」按钮，即可在弹出的多行文本框执行文本编辑操作，该功能可以用于导入或导出歌词。
    </NP>
