    <NP>
        Intellyricist 歌词生成器具有两个模型，一个模型可以由关键词生成歌词（代号为 K-模型），另一个模型只能由已有的歌词续写歌词（代号为 P-模型）。如果选中，那么程序将会使用 K-模型生成歌词；否则程序将会使用 P-模型生成歌词。
    </NP>
