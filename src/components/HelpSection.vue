    </NP>
    <NH2>行 Prompt</NH2>
    <NP>
        「行 Prompt」文本框中的文字将会作为程序所生成歌词的第一行的前缀，换言之，程序所生成歌词的第一行将会在「行 Prompt」文本框中的文字的基础上续写。
    </NP>
    <NH2>高级参数</NH2>
    <NP>一般来说，高级参数无需修改。</NP>
